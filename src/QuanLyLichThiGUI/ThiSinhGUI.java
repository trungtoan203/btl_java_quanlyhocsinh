    private void btnXoaThiSinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaThiSinhActionPerformed
        int[] selectedRows = tableThiSinh.getSelectedRows();
        boolean checkRemove = false;
        if (selectedRows.length <= 0) {
            JOptionPane.showMessageDialog(this, "Hãy chọn ít nhất một dòng để xóa!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa các dòng đã chọn?", "Xác nhận xóa", JOptionPane.INFORMATION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION) {
                List<String> needToRemove = new ArrayList<>();
                for (int row : selectedRows) {
                    needToRemove.add(lichthi.getListHS().get(row));
                }
                for (LichThi lich : listLichThi) {
                    if (lich.equals(this.lichthi)) {
                        lich.xoaHS(needToRemove);
                        break;
                    }
                }
                showTableDataTS();
                JOptionPane.showMessageDialog(this, "Xóa thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                try {
                    writeFile(fileName, listLichThi);
                } catch (Exception ex) {
                    Logger.getLogger(QuanLyLichThiGUI.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_btnXoaThiSinhActionPerformed

