    /**
     * @param args the command line arguments
     */
    public void showTableDataTS() {
        tbThiSinh.setRowCount(0);
        List<HocSinh> listShow = new ArrayList<>();
        for (int i = 0; i < listMHS.size(); i++) {
            listShow.add(getListSVById(listHS, listMHS.get(i)));
        }
        for (HocSinh sv : listShow) {
            tbThiSinh.addRow(new Object[]{sv.getMaHS(), sv.getHoTen(), sv.getGioiTinh(), sv.getLop(), sv.getCongTacDoan()});
        }
    }

